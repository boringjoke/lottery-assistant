CREATE TABLE lottery_favorite_draw_results (
    id BIGINT NOT NULL AUTO_INCREMENT COMMENT '主键 ID',
    favorite_id BIGINT NOT NULL COMMENT '收藏号码 ID，由业务代码保证关联有效',
    user_id BIGINT NOT NULL COMMENT '用户 ID，冗余便于权限隔离和分页查询',
    draw_id BIGINT NOT NULL COMMENT '开奖主表 ID，由业务代码保证关联有效',
    lottery_type VARCHAR(16) NOT NULL COMMENT '彩票类型编码，MVP 固定为 DLT',
    issue_no VARCHAR(32) NOT NULL COMMENT '开奖期号',
    draw_date DATE NOT NULL COMMENT '开奖日期',
    favorite_front_numbers VARCHAR(32) NOT NULL COMMENT '收藏前区号码快照，升序逗号分隔',
    favorite_back_numbers VARCHAR(16) NOT NULL COMMENT '收藏后区号码快照，升序逗号分隔',
    draw_front_numbers VARCHAR(32) NOT NULL COMMENT '开奖前区号码快照，升序逗号分隔',
    draw_back_numbers VARCHAR(16) NOT NULL COMMENT '开奖后区号码快照，升序逗号分隔',
    front_hit_count INT NOT NULL DEFAULT 0 COMMENT '前区命中数量',
    back_hit_count INT NOT NULL DEFAULT 0 COMMENT '后区命中数量',
    winning TINYINT(1) NOT NULL DEFAULT 0 COMMENT '是否中奖',
    prize_level INT NULL COMMENT '奖级等级，1-9；未中奖为空',
    prize_name VARCHAR(64) NOT NULL COMMENT '奖级名称，如 一等奖、九等奖、未中奖',
    rule_version VARCHAR(32) NOT NULL COMMENT '奖级规则版本，如 DLT_2019',
    stake_amount DECIMAL(18,2) NULL COMMENT '单注奖金，未知时为空',
    calculated_time DATETIME NOT NULL COMMENT '计算时间',
    create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    PRIMARY KEY (id),
    UNIQUE KEY uk_favorite_draw (favorite_id, draw_id),
    KEY idx_user_draw_date (user_id, draw_date, issue_no),
    KEY idx_user_favorite (user_id, favorite_id),
    KEY idx_lottery_issue (lottery_type, issue_no),
    KEY idx_draw_id (draw_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='收藏号码开奖结果表';
